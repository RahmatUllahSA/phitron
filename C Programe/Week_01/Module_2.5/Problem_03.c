/*
In this problem you will be given an integer number N. You will have to add 5 with N and print it.

Input Format:
Input consists of an integer number N.

Constraints:
1 <= N <= 100

Output Format:
Output the result after adding 5 with N.

Sample Input:
100

Sample Output:
105
*/






#include<stdio.h>
int main()
{
    int N;
    scanf("%d", &N);
    printf("%d\n",N+5);
    return 0;
}